import re

# Расширенный список запрещённых слов (русский мат, насилие, террор, война и др.)
PROHIBITED_WORDS = [
    "хуй", "пизда", "ебать", "блядь", "сука", "мразь", "нахуй", "уебок", "пидор", "долбоёб",
    "террор", "теракт", "взорвать", "бомба", "убить", "расстрелять", "война", "украинец", "русский", "еврей",
    "гей", "лесбиянка", "нацист", "гитлер", "путин", "зеленский", "азов", "наци", "москаль",
    "иди нахуй", "сдохни", "зарежу", "порвёт", "изнасилую", "обоссу", "сожгу"
]

pattern = re.compile(r"\\b(?:" + "|".join(re.escape(word) for word in PROHIBITED_WORDS) + r")\\b", re.IGNORECASE)

def contains_prohibited_content(text: str) -> bool:
    return bool(pattern.search(text))

